#include <stdio.h>

//Compiler version gcc  6.3.0

int main()
{
  int day=7;
  printf("Enter the number");
  scanf("%d",&day);
  switch (day)
  {
    case 1:
      
      printf("monday");
      break;
      case 2:
     
      printf("Tuesday");
      break;
      
      case 3:
      
      printf("Wednesday");
      break;
      
  case 4:
      
      printf("Thursday");
      break;
      case 5:
      
      printf("Friday");
      break;
      case 6:
      
      printf("Saturday");
      break;
      case 7:
      
      printf("sunday");
      break;
      }
  return 0;
}
